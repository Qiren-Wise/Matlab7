%非线性拟合
x=1:16;
y=[4.00 6.40 8.00 8.80 9.22 9.50 9.70 9.86
10.00 10.20 10.32 10.42 10.52 10.55 10.58 10.60];
y1=@(b,t)b(1)*exp(-t/b(2))+b(3)*exp(-t/b(4))+b(5); %拟合函数 5个初值  拟合是指数函数
                                       %b 拟合参数 t拟合残差  
                                       %@ 函数表达式
b0=[-1 1 -1 1 0];
%a=nlinfit(x,y,y1,b0);
a = polyfit(x,y,y1,b0); %自变量 因变量 拟合函数 初始迭代值  
xp=1:0.1:16;
yp=y1(a,xp);
plot(x,y,'.k',xp,yp,'r');
